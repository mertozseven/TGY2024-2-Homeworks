 -static keyword'ü neden ne zaman kullanılır?
 
    static anahtar kelimesi, genellikle bir sınıfın tüm nesneleri arasında paylaşılan değişkenler veya metotlar tanımlamak için kullanılır. static kelimesi ile tanımlanan değişkenler ve metotlar, sınıfa ait olup, sınıfın herhangi bir nesnesine ait değildir. Bu, bellek yönetimini optimize etmek ve kodun okunabilirliğini ve anlaşılabilirliğini artırmak için kullanışlıdır
 
 
 - Bound ve frame arasında ki fark nedir?
    
    Frame: Bir view'ın parent view'a göre koordinatlarıdır. Yani, bir view'ın parent view içindeki konumunu ve boyutunu belirtir.
    Bounds: Bir view'ın kendi koordinat sistemine göre koordinatlarıdır. Yani, bir view'ın kendi içindeki konumunu ve boyutunu belirtir. Bounds, view'ın döndürülmesi veya ölçeklendirilmesi gibi dönüşümlerden etkilenmez.
 
